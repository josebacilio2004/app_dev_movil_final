  /// Helper para construir el map de un producto con campos de búsqueda normalizados
  static Map<String, dynamic> _producto({
    required String nombre,
    required String descripcion,
    required String categoria,
    required String subcategoria,
    required double precioUnitario,
    required double precioMayorista,
    required String marca,
    required String unidad,
    required String sku,
    required List<String> tags,
  }) {
    return {
      'nombre': nombre,
      'descripcion': descripcion,
      'categoria': categoria,
      'subcategoria': subcategoria,
      'precio_unitario': precioUnitario,
      'precio_mayorista': precioMayorista,
      'marca': marca,
      'unidad': unidad,
      'codigo_sku': sku,
      'disponible': true,
      'stock_minimo': 5,
      'tags': tags,
      'imagen_url': null,
      // Campos de búsqueda normalizados (sin acentos, minúsculas)
      'nombre_busqueda': _normalizar(nombre),
      'marca_busqueda': _normalizar(marca),
      'categoria_busqueda': _normalizar(categoria),
    };
  }

  static String _normalizar(String texto) {
    String r = texto.toLowerCase().trim();
    const acentos = {
      'á': 'a', 'é': 'e', 'í': 'i', 'ó': 'o', 'ú': 'u',
      'ñ': 'n',
    };
    acentos.forEach((k, v) => r = r.replaceAll(k, v));
    return r;
  }
